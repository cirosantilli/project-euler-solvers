# ---------- Big-int packed convolution (exact, no carries) ----------
BASE_BITS = 72
BASE_MASK = (1 << BASE_BITS) - 1


def _pack(coeffs):
    """Pack coefficients into a single big integer in base 2^BASE_BITS (little-endian)."""
    x = 0
    for c in reversed(coeffs):
        x = (x << BASE_BITS) + c
    return x


def _unpack(x, n):
    """Unpack n coefficients from a packed big integer."""
    out = [0] * n
    for i in range(n):
        out[i] = x & BASE_MASK
        x >>= BASE_BITS
    return out


def _conv(a, b):
    """Convolution of coefficient lists a and b (exact integer coefficients)."""
    return _unpack(_pack(a) * _pack(b), len(a) + len(b) - 1)


def _conv_with_const(a, packed_b, len_b):
    """Convolution where the second operand is already packed."""
    return _unpack(_pack(a) * packed_b, len(a) + len_b - 1)


# ---------- Polynomial inverse mod x^k (Newton iteration) ----------
def _poly_inv_mod_xk(f, k):
    """
    Invert polynomial f modulo x^k, assuming f[0] != 0.
    Returns g such that f * g ≡ 1 (mod x^k).
    """
    inv0 = pow(f[0], MOD - 2, MOD)
    g = [inv0]
    m = 1
    while m < k:
        m2 = 2 * m
        if m2 > k:
            m2 = k

        # t = f*g mod x^m2
        t = _conv(f[:m2], g)[:m2]
        for i in range(m2):
            t[i] %= MOD

        # t = (2 - t) mod MOD
        t[0] = (2 - t[0]) % MOD
        for i in range(1, m2):
            t[i] = (-t[i]) % MOD

        # g = g*t mod x^m2
        g = _conv(g, t)[:m2]
        for i in range(m2):
            g[i] %= MOD

        m = m2
    return g


# ---------- Build modulus polynomial C(x) from REC ----------
# Characteristic polynomial:
#   x^L - REC[0] x^(L-1) - ... - REC[L-1]
C = [0] * (L + 1)
C[L] = 1
for i, c in enumerate(REC):
    C[L - 1 - i] = (-c) % MOD

# Reverse polynomial for fast division-based reduction
CR = list(reversed(C))  # CR[0] == 1
INV_CR = _poly_inv_mod_xk(CR, K)
_PACKED_INV_CR = _pack(INV_CR)
_PACKED_C = _pack(C)
_LEN_INV_CR = len(INV_CR)
_LEN_C = len(C)


def _poly_mod(P):
    """
    Reduce polynomial P (length 2L-1, coefficients already modded) modulo C to degree < L.
    Uses reversed-division with precomputed inverse of CR modulo x^K.
    """
    # Compute quotient q of degree <= L-2. Work with reversed polynomials.
    PR = P[::-1]
    q_rev = _conv_with_const(PR[:K], _PACKED_INV_CR, _LEN_INV_CR)[:K]
    for i in range(K):
        q_rev[i] %= MOD
    q = q_rev[::-1]

    # Subtract q*C; only the first L coefficients are needed for the remainder.
    QC = _conv_with_const(q, _PACKED_C, _LEN_C)
    R = [0] * L
    for i in range(L):
        R[i] = (P[i] - QC[i]) % MOD
    return R


def _poly_mul_mod(a, b):
    """Multiply two polynomials of degree < L in the quotient ring modulo C."""
    P = _conv(a, b)  # length 2L-1
    for i in range(2 * L - 1):
        P[i] %= MOD
    return _poly_mod(P)


def _poly_pow(base, exp):
    """Exponentiate a polynomial (degree < L) in the quotient ring."""
    res = [0] * L
    res[0] = 1
    b = base
    e = exp
    while e:
        if e & 1:
            res = _poly_mul_mod(res, b)
        e >>= 1
        if e:
            b = _poly_mul_mod(b, b)
    return res


def T_small(n):
    """Compute T(n) for small n using the binomial transform directly."""
    total = 0
    comb = 1
    for k in range(0, n + 1):
        total = (total + comb * E_INIT[k]) % MOD
        if k != n:
            comb = comb * (n - k) % MOD
            comb = comb * pow(k + 1, MOD - 2, MOD) % MOD
    return total


def solve(n):
    # Build (1+x)^n modulo C in the quotient ring
    base = [0] * L
    base[0] = 1
    base[1] = 1  # 1 + x
    R = _poly_pow(base, n)

    # Dot with initial E values: T(n) = sum R[i]*E(i)
    s = 0
    for i in range(L):
        s = (s + R[i] * E_INIT[i]) % MOD
    return s


